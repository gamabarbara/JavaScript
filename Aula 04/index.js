let random = Math.random() * 300 + 2 

console.log(random) // random gera números entre zero e um
console.log(Math.floor(random)) //Arredonda o número para baixo
console.log(Math.ceil(random)) //Arredonda o número para cima
console.log(Math.round(random)) //Arredonda o número

