/* OPERADOR PÓS INCREMENTO */
let x = 0
console.log(x++) // valor atual e depois incrementa
console.log(x++) // valor atual e depois incrementa
console.log(x) // mostra o valor atualizado/incrementado

/* OPERADOR PRÉ INCREMENT0 */
console.log(++x) //incrementa e já mostra o valor atualizado

/* OPERADOR PÓS DECREMENTO */
console.log(x--) // mostra o valor antigo e depois decrementa
console.log(--x) // decrementa e mostra o resultado atualizado